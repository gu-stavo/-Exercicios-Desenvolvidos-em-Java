package poofatec;

public class ExemploWhile {

    public static void main(String[] args) {

        Integer n = 0;
        int N = 0;
        
        while(n <=10){
            //bloco de comandos
             System.out.println(n);
             
             n++;
             
        }
        System.out.println("--------------------------\n");
        while(N <=10){
            //bloco de comandos
             System.out.println(N);
             
             N+=2;
             
        }
        
        
    }

}
